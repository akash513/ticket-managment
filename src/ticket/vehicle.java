package ticket;

public class vehicle {
		public String name;
                public int car_no;
                public int ticket_price;
		public String destination;
	public vehicle()
	{
		
	}
	public vehicle(String na,
                int cn,
                int tp,
                String ds)
	{
		this.name = na;
		this.car_no = cn;
		this.ticket_price = tp;
		this.destination = ds;
	}

}